int		ft_iterative_factorial(int nb)
{
	int i;
	int res;

	res = 1;
	i = 2;
	if (nb < 0 || nb >= 13)
		return (0);
	while (i <= nb)
	{
		res = res * i;
		i++;
	}
	return (res);
}
